import math
x = 10

# find factorial of x and print the result
print("Factorial of 10",math.factorial(x))

# find math.floor and print the result
print("Floor of  10",math.floor(x))

# add x three times and print the result
k=math.floor(x)
# print(k)
print("Sum of 10 three times",3.0*k)


a = 5
b = 6

# find GCD of a and b and print the result
print(f'GCD of {a} and {b} {math.gcd(a,b)}')

# find math.isfinite and print the result
print(f'Is {x} a finite number: {math.isfinite(x)}')

# find math.isinf and print the result
print(f'Is {x} a infinite number: {math.isinf(x)}')