mytuple = ("this", 10.0, "is", "float", 3.6)

# Print value at index 0
print(mytuple[0])
# Print value at index 1
print(mytuple[1])
# Print value at index -1
print(mytuple[-1])
# Print all the values from index 0
print(mytuple)
# Print all the values except the last value
print(mytuple[0:-1])
print(mytuple[::-1])